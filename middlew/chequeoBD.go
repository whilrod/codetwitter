package middlew

import (
	"net/http"
	"src/codetwitter/bd"
)

func ChequeoBD(next http.HandlerFunc) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		if bd.ChequeoConection() == 0 {
			http.Error(w, "Conexi√≥n perdida con la base de datos", 500)
			return
		}
		next.ServeHTTP(w, r)
	}
}
